#include "3-calc.h"
#include <stdlib.h>

/**
 * op_add - Function that returns the add of two integers
 * @a: Int 1
 * @b: Int 2
 * Return: The add
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function that returns the subtraction of two integers
 * @a: Int 1
 * @b: Int 2
 * Return: The difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that returns the product of two integers
 * @a: Int 1
 * @b: Int 2
 * Return: The product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function that returns the division of two integers
 * @a: Int 1
 * @b: Int 2
 * Return: The division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that returns the mod of two integers
 * @a: Int 1
 * @b: Int 2
 * Return: The mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
